from fpdf import FPDF
import datetime

def export_to_pdf(mkt_text, comp_text, swot_text, strat_text):
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Arial", "B", 16)
    pdf.cell(200, 10, "Market Research Report", ln=True, align="C")
    pdf.set_font("Arial", "", 10)
    pdf.cell(200, 10, f"Generated: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M')}", ln=True, align="C")
    pdf.ln(5)

    for title, content in [("Market Analysis", mkt_text),
                            ("Competitor Analysis", comp_text),
                            ("SWOT Analysis", swot_text),
                            ("Strategy", strat_text)]:
        pdf.set_font("Arial", "B", 12)
        pdf.cell(200, 10, title, ln=True)
        pdf.set_font("Arial", "", 10)
        # Handle special characters
        safe_content = (content or "").encode('latin-1', 'replace').decode('latin-1')
        pdf.multi_cell(0, 8, safe_content)
        pdf.ln(3)

    filename = f"market_report_{datetime.datetime.now().strftime('%Y%m%d_%H%M%S')}.pdf"
    pdf.output(filename)
    return filename

