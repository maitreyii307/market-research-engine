_EMBEDDINGS = None

def get_embeddings():
    global _EMBEDDINGS
    if _EMBEDDINGS is None:
        print("⏳ Loading embedding model (~30 seconds first time)...")
        _EMBEDDINGS = HuggingFaceEmbeddings(model_name="all-MiniLM-L6-v2")
        print("✅ Embedding model ready!")
    return _EMBEDDINGS

def get_llm(temperature):
    api_key = os.environ.get("GROQ_API_KEY", "").strip()
    return ChatGroq(
        model="llama-3.1-8b-instant",
        temperature=temperature,
        groq_api_key=api_key,
    )

def build_vectorstore(file_obj):
    if file_obj is None:
        return None
    path = file_obj if isinstance(file_obj, str) else getattr(file_obj, "name", None)
    if not path:
        return None
    try:
        loader = PyPDFLoader(path) if path.lower().endswith(".pdf") else TextLoader(path)
        docs   = loader.load()
        chunks = RecursiveCharacterTextSplitter(
            chunk_size=600, chunk_overlap=60
        ).split_documents(docs)
        return FAISS.from_documents(chunks, get_embeddings())
    except Exception as e:
        print(f"⚠️ Document error: {e}")
        return None

def retrieve_context(vs, query):
    if vs is None:
        return "No document uploaded."
    try:
        return "\n\n".join(d.page_content for d in vs.similarity_search(query, k=5))
    except Exception:
        return "Could not retrieve context."

def error_result(msg):
    return msg, "", "", "", "❌ Failed", ""

