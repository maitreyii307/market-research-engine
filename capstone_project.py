def run_analysis(industry, business_idea, file_obj):

    # --- Load API Key from Colab Secrets ---
    try:
        api_key = userdata.get("GROQ_API_KEY")
        if not api_key or not api_key.strip():
            return error_result("❌ Groq API key not found. Please add it to Colab Secrets under the name GROQ_API_KEY.")
        os.environ["GROQ_API_KEY"] = api_key.strip()
        print(f"✅ Groq API key loaded (starts with: {api_key[:8]})...")
    except Exception as e:
        return error_result(f"❌ Could not read Groq API key from Colab Secrets: {e}")

    # --- Check that user filled in the fields ---
    industry      = str(industry).strip()
    business_idea = str(business_idea).strip()
    if not industry or not business_idea:
        return error_result("⚠️ Please fill in both the Industry and Business Idea fields.")

    # --- Handle uploaded document ---
    rag_status = "ℹ️ No document — using LLM knowledge only"
    vs = None
    if file_obj is not None:
        print("📄 Processing uploaded document...")
        vs = build_vectorstore(file_obj)
        rag_status = "✅ Document indexed!" if vs else "⚠️ Document failed — using LLM only"

    rag_ctx = retrieve_context(vs, f"{industry} {business_idea}")

    # --- First AI Call: Market Research ---
    print("🔍 Calling AI for market research...")
    try:
        raw = (
            ChatPromptTemplate.from_messages([("human", RESEARCH_HUMAN)])
            | get_llm(0.2)
            | StrOutputParser()
        ).invoke({
            "industry":      industry,
            "business_idea": business_idea,
            "rag_context":   rag_ctx,
        })
        print("✅ Research done!")
    except Exception as e:
        traceback.print_exc()
        err = str(e)
        if any(x in err.upper() for x in ["API_KEY", "INVALID", "401", "403", "AUTHENTICATION"]):
            return error_result("❌ Groq rejected your API key. Please check your GROQ_API_KEY in Colab Secrets.")
        return error_result(f"❌ AI error: {err}")

    # --- Parse the JSON response ---
    cleaned = raw.strip()
    for fence in ["```json", "```JSON", "```"]:
        cleaned = cleaned.replace(fence, "")
    start = cleaned.find("{")
    end   = cleaned.rfind("}") + 1
    if start == -1 or end <= start:
        return error_result(f"❌ No JSON found in response.\n\nRaw:\n{raw[:500]}")
    try:
        data = json.loads(cleaned[start:end])
    except json.JSONDecodeError as e:
        return error_result(f"❌ JSON parse error: {e}")

    # --- Extract the data pieces ---
    swot        = data.get("swot_analysis") or {}
    competitors = data.get("top_competitors") or []
    market_size = data.get("market_size", "N/A")
    if not isinstance(swot, dict):        swot = {}
    if not isinstance(competitors, list): competitors = []

    swot_summary = (
        f"Strengths: {swot.get('strengths','N/A')} | "
        f"Weaknesses: {swot.get('weaknesses','N/A')} | "
        f"Opportunities: {swot.get('opportunities','N/A')} | "
        f"Threats: {swot.get('threats','N/A')}"
    )
    swot_display = (
        f"✅ STRENGTHS:\n{swot.get('strengths','N/A')}\n\n"
        f"⚠️  WEAKNESSES:\n{swot.get('weaknesses','N/A')}\n\n"
        f"🌱 OPPORTUNITIES:\n{swot.get('opportunities','N/A')}\n\n"
        f"🔴 THREATS:\n{swot.get('threats','N/A')}"
    )
    competitors_display = (
        "\n".join(f"{i+1}. {c}" for i, c in enumerate(competitors))
        or "No competitors returned."
    )

    # --- Second AI Call: Go-to-Market Strategy ---
    print("🚀 Generating go-to-market strategy...")
    try:
        strategy = (
            ChatPromptTemplate.from_messages([("human", STRATEGY_HUMAN)])
            | get_llm(0.8)
            | StrOutputParser()
        ).invoke({
            "industry":      industry,
            "business_idea": business_idea,
            "market_size":   market_size,
            "competitors":   ", ".join(competitors[:3]),
            "swot_summary":  swot_summary,
        })
        print("✅ Strategy done!")
    except Exception as e:
        traceback.print_exc()
        strategy = f"⚠️ Strategy failed: {e}"

    # --- Return all results to the UI ---
    return (
        market_size,
        competitors_display,
        swot_display,
        strategy,
        rag_status,
        json.dumps(data, indent=2),
    )

